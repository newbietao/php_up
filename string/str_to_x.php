<?php
// string to lower
$str = "HELLO WORLD";
var_dump($str);
var_dump(strtolower($str));
// string to upper
$str = "hello world";
var_dump($str);
var_dump(strtoupper($str));
// string to ucfirst
$str = "hello world";
var_dump($str);
var_dump(ucfirst($str));
// string to ucwords
var_dump(ucwords($str))

?>
